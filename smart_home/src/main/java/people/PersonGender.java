package people;

/**
 * Enumerates various gender identities.
 * This enum is used to represent a person's gender identity, acknowledging the diversity and inclusivity of different gender expressions.
 * It includes options ranging from traditional binary genders to various non-binary and genderqueer identities.
 */
public enum PersonGender {
    MALE,
    FEMALE,
    TRANSGENDER,
    NONBINARY,
    GENDERQUEER,
    GENDERFLUID,
    AGENDER,
    BIGENDER,
    DEMIBOY,
    DEMIGIRL,
    GENDERVARIANT,
    GENDERNONCONFORMING,
    TWOSPIRIT,
    THIRDGENDER,
    NEUTROIS
}
